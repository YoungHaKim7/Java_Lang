public class Main {
  public static void main(String[] args) {
    int max = Integer.MAX_VALUE;
    int min = Integer.MIN_VALUE;

    System.out.println("The maximum value for an int = " + max);
    System.out.println("The minimum value for an int = " + min);

    byte maxByteValue = Byte.MAX_VALUE;
    byte minByteValue = Byte.MIN_VALUE;

    System.out.println("The maximum value for an byte = " + maxByteValue);
    System.out.println("The minimum value for an byte = " + minByteValue);
  }
}
